
--- A structure that holds (sometimes redundant) information on a soul gem.
---@class tes3soulGemData
tes3soulGemData = {}

--- The value of the object.
---@type number
tes3soulGemData.value = nil

--- A reference back to the misc item that the soul gem represents.
---@type tes3misc
tes3soulGemData.item = nil

--- The weight, in pounds, of the object.
---@type number
tes3soulGemData.weight = nil

--- The unique object ID.
---@type string
tes3soulGemData.id = nil

--- The player-facing name for the object.
---@type string
tes3soulGemData.name = nil

--- The path to the object's mesh.
---@type string
tes3soulGemData.mesh = nil

--- The path to the object's texture.
---@type string
tes3soulGemData.texture = nil


